// function named cleanSet that returns a string of
// all the set values that start with a specific string (startString).
export default function cleanSet() {
  const maps = new Map([
    ['Apples', 10],
    ['Tomatoes', 10],
    ['Pasta', 1],
    ['Rice', 1],
    ['Banana', 5],
  ]);
  return maps;
}
